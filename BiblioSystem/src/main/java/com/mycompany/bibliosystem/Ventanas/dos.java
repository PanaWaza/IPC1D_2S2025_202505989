    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        // -- guardar datos en variables
        String name = nombre.getText();
        String carne = carnet.getText();
        int Ncarne = Integer.parseInt(carne);
        String contra = password.getText();
        String carrera = carr.getText();
        
        /*
        llamar un metodo para examinar los datos del registrando 
        el metodo que devuelva un true or false para mostrar un mensaje de alerta si no se puede registrar
        */
        
        // declaracion para usar metodo de otro paquete
        
        boolean existe = Estudiante.DatosRegistro(name, Ncarne, contra, carrera, true);
        if ( existe == true) {
            JOptionPane.showMessageDialog(this,
                    " El carnet ya existe ",
                    " ERROR:",JOptionPane.ERROR_MESSAGE);
            
            String carnet = Integer.toString(Ncarne);
            Bitacora.appendBitacora(OpB+" fallido "+"carnet existente", carnet, MoB);
        }
        else {
            JOptionPane.showMessageDialog(this, 
                    " Usuario creado correctamente, por favor guarda tus credenciales",
                    " Usuario registrado:",
                    JOptionPane.INFORMATION_MESSAGE);
            EscrituraArchivoCuentas.IngresarEstudiante(name, Ncarne, contra, carrera, 0, 0);
            
            this.dispose(); // cerrar ventana antes de la siguiente ventana
            
            String carnet = Integer.toString(Ncarne);
            Bitacora.appendBitacora(OpB+" exitosso ", carnet, MoB);
            
            uno window = new uno();
            window.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

