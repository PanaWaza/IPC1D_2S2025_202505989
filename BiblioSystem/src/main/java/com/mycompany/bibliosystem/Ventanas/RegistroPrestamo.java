    private boolean esNumerico(String str) {
    if (str == null || str.isEmpty()) {
        return false;
    }
    for (char c : str.toCharArray()) {
        if (!Character.isDigit(c)) {
            return false;
        }
    }
    return true;
}
       
    private void asignarPrestamo() {
    String Tcarnet = Texto1.getText().trim();
    String ibn = Texto2.getText().trim();

    // 1. Validar campos vacíos
    if (Tcarnet.isEmpty() || ibn.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        Bitacora.appendBitacora("Asignacion de prestamo fallido (falta de parametros)", Tcarnet , MoB);
        return;
    }

    // 2. Validar que el carnet sea un número (Sin try-catch)
    if (!esNumerico(Tcarnet)) {
        JOptionPane.showMessageDialog(this, "El carnet debe contener solo números", "Error", JOptionPane.ERROR_MESSAGE);
        Bitacora.appendBitacora("Asignacion de prestamo fallido (el carnet tiene letras)", Tcarnet , MoB);
        return;
    }

    int carnet = Integer.parseInt(Tcarnet);
    String bookName = Libro.getBookName(ibn);

    // 3. Validar si el libro existe
    if (bookName == null || bookName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El ISBN ingresado no existe", "Error", JOptionPane.ERROR_MESSAGE);
        Bitacora.appendBitacora("Asignacion de prestamo fallido (isbn no existe)", Tcarnet , MoB);
        return;
    }

    // 4. Procesar préstamo
    String fechaP = Libro.getDatePrestamo();
    String fechaD = Libro.SumaFecha(fechaP); // Aquí es donde sumas los 15 días

    prestamo.guardarPrestamo(carnet, ibn, bookName, fechaP, fechaD, "activo");
    Libro.RestaEjemplares(ibn);

    String historial = prestamo.getEstadoLibro(carnet, ibn);
    
    JOptionPane.showMessageDialog(this, "Se prestó el libro correctamente", "Operación exitosa", JOptionPane.INFORMATION_MESSAGE);
    Bitacora.appendBitacora("Asignacion de prestamo exitoso", Tcarnet , MoB);
    EscrituraPrestamos.RegistrarPrestamo(Tcarnet, ibn, bookName, fechaP, fechaD, historial);
    
}
    
    private void devolver() {
    // 1. Obtener el índice de la fila seleccionada
    int filaSeleccionada = TablaUsuario.getSelectedRow();
    
    String TextIbn = TablaUsuario.getValueAt(filaSeleccionada, 1).toString();
    String TextCarnet = CarnetTabla.getText().trim();
    

    // 2. Validar que el usuario haya seleccionado una fila
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione un libro de la tabla para devolver.", "Aviso", JOptionPane.WARNING_MESSAGE);
        Bitacora.appendBitacora("Asignacion de prestamo fallido (no se selecciono una fila en la tabla)", TextCarnet , MoB);
        return;
    }
    

    if (TextCarnet.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el carnet del estudiante primero.", "Error", JOptionPane.ERROR_MESSAGE);
        Bitacora.appendBitacora("Asignacion de prestamo fallido (no se ingreso el carnet )", "vacio" , MoB);
        return;
    }
    
    if (!esNumerico(TextCarnet)) {
        JOptionPane.showMessageDialog(this, "El carnet debe ser numérico.", "Error", JOptionPane.ERROR_MESSAGE);
        Bitacora.appendBitacora("Asignacion de prestamo fallido (el carnet no es numerico)", TextCarnet , MoB);
        return;
    }

    int carnetInt = Integer.parseInt(TextCarnet);

    // 5. Verificar préstamos vencidos (opcional según tu lógica)
    if (prestamo.BuscarPrestamosVencidos(carnetInt)) {
        JOptionPane.showMessageDialog(this, "El estudiante tiene préstamos vencidos.", "Error", JOptionPane.ERROR_MESSAGE);
        Bitacora.appendBitacora("Asignacion de prestamo fallido (El estudiante tiene prestamos vencidos)", TextCarnet , MoB);
        return;
    }

    // 6. Ejecutar la devolución en la "base de datos" (arreglos)
    prestamo.EliminarPrestamo(TextCarnet, TextIbn); // Cambia el estado a "devuelto"
    Libro.SumaEjemplares(TextIbn); // Devuelve el libro al inventario

    JOptionPane.showMessageDialog(this, "Se devolvió el libro correctamente", "Operación exitosa", JOptionPane.INFORMATION_MESSAGE);

    EscrituraPrestamos.EliminarPrestamo(TextCarnet, TextIbn);
    Bitacora.appendBitacora("Asignacion de prestamo exitoso", TextCarnet , MoB);
    // 7. Actualizar la tabla para que el libro desaparezca (porque ya no es "activo")
    MostrarDatosTabla(TextCarnet);
}
    
    
