    private void MostrarDatosTabla(String carnet){
        
        DefaultTableModel modelo = (DefaultTableModel) TablaUsuario.getModel();
        
        modelo.setRowCount(0);
        
        Object[][] fila = prestamo.ObtenerPrestamosActivos(carnet);
        
        Object[] datos = new Object[5];
        
        for (int i = 0; i < 3; i++) {
            if (fila != null ) {
                datos[0] = fila[i][0];
                datos[1] = fila[i][1];
                datos[2] = fila[i][2];
                datos[3] = fila[i][3];
                datos[4] = fila[i][4];
                modelo.addRow(datos);
            }
        }
    }
    
